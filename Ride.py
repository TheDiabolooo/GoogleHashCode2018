class Ride():

    def __init__(self, a, b, x, y, s, f):
        self.a = a
        self.b = b
        self.x = x
        self.y = y
        self.s = s
        self.f = f
   

    def getA():
        return self.a
    def getB():
        return self.b
    def getX():
        return self.x
    def getY():
        return self.y
    def getS():
        return self.s
    def getF():
        return self.f
