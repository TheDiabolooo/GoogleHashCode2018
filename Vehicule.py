class Vehicule():
    def __init__(self, x, y, ride, available, step):
        self.x = x
        self.y = y
        self.ride = ride
        self.available = available
        self.step = step
    
   
    def getX():
        return self.x
    def getY():
        return self.y
    def getRide():
        return self.ride
   
