class Map():

    def __init__(self, R, C, steps, vehicule, ride):
        self.R = R
        self.C = C
        self.steps = steps
        self.vehicule = vehicule
        self.ride = ride
        

    def getR():
        return self.R
    def getC():
        return self.C
    def getSteps():
        return self.steps
    def getVehicule():
        return self.vehicule
    def getRide():
        return self.ride
